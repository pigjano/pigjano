function buildDoseMessages(seed, targetCount = 100) {
  const messages = [];

  for (const opener of seed.openers) {
    for (const action of seed.actions) {
      for (const closer of seed.closers) {
        messages.push(`${opener} ${action} ${closer}`);
        if (messages.length === targetCount) return messages;
      }
    }
  }

  return messages;
}

